const getDishes = (content) => {
  const start = content.indexOf('const dishes = [');
  const end = content.indexOf('];let currentLanguage = \'ml\';', start) + 2;
  const dishesStr = content.substring(start, end);
  return eval(dishesStr.substring(15));
};

const scriptContent = fs.readFileSync('script.js', 'utf8');
const scriptDishes = getDishes(scriptContent);

let log = '';
const print = (msg) => { log += msg + '\n'; console.log(msg); };

for (let i = 0; i < scriptDishes.length; i++) {
  const d1 = scriptDishes[i];
  const d2 = approved[i];
  
  print(`\n--- Checking Recipe ${i+1}: ${d2.name} ---`);
  if (d1.ml.name !== d2.name) {
    print(`  Name differs:`);
    print(`    Current : "${d1.ml.name}"`);
    print(`    Approved: "${d2.name}"`);
  }
  
  // Ingredients
  for (let j = 0; j < Math.max(d1.ml.ingredients.length, d2.ingredients.length); j++) {
    const ing1 = d1.ml.ingredients[j];
    const ing2 = d2.ingredients[j];
    if (ing1 !== ing2) {
      print(`  Ingredient ${j+1} differs:`);
      print(`    Current : "${ing1}"`);
      print(`    Approved: "${ing2}"`);
    }
  }
  
  // Steps
  for (let j = 0; j < Math.max(d1.ml.steps.length, d2.steps.length); j++) {
    const step1 = d1.ml.steps[j];
    const step2 = d2.steps[j];
    if (step1 !== step2) {
      print(`  Step ${j+1} differs:`);
      print(`    Current : "${step1}"`);
      print(`    Approved: "${step2}"`);
    }
  }
}

fs.mkdirSync('scratch', { recursive: true });
